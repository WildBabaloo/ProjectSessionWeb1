    <p style = "font-size: 20px;">Plus de 22 millions de professionnels de l'informatique, de petites entreprises, de gouvernements locaux, <br>
    d'étudiants, d'ingénieurs, de programmeurs, de fabricants, de passionnés de technologie, de joueurs,<br>
    de clients de produits informatiques et d'appareils électroniques ont fait confiance à Rosemont Tech <br>
    pour leurs besoins d'assistance en matière de communication et de technologies de l'informatique depuis 1979.</p>

      <p style="font-size: 20px;">Rosemont Tech fournit les services et la technologie essentiels pour soutenir la santé, <br>
        la sécurité et le bien-être de nos communautés. Maintenant qu'un Canadien sur 5 a reçu l'ordre de rester à la maison, <br>
         Rosemont Tech peut vous fournir les produits et services de support technologique essentiels pour travailler, <br>
          apprendre et recevoir la télémédecine depuis la sécurité de votre maison. <br>
          Nous sommes profondément passionnés par la protection de la santé et de la sécurité du public pendant cette période dans notre pays.</p>
      
      <p style="font-size: 20px;">Rosemont Tech est l'un des principaux fournisseurs de technologies de l'informatique, de communications <br>
         et d'appareils électroniques du pays, exploitant vingt-cinq grands magasins dans les principaux marchés du pays. <br>
         Fondé en 1979 à Montréal, Québec, Rosemont Tech a connu une croissance constante et rentable. <br> 
         Nos magasins sont conçus pour répondre aux besoins d'une grande variété de consommateurs - des petites entreprises <br>
         aux écoles locales - avec des associés bien formés qui offrent des solutions de technologie de l'informatique.</p>

        <p style="font-size: 20px;">Centré uniquement sur les produits de technologie de l'informatique, Rosemont Tech propose plus d'ordinateurs, <br>
        d'électronique, de dispositifs de réseautage et de communication (plus de 30 000 articles en stock) que toute autre entreprise. <br>
        Rosemont Tech est profondément passionné par la fourniture de produits de technologie de l'informatique <br>
        et de services de support technologique. Nous proposons le ramassage en magasin des commandes en ligne depuis 2010. <br>
        Les consommateurs peuvent visiter le seul magasin Rosemont Tech d'un océan à l'autre ou RosemontTech.com <br>
         pour des milliers d'articles informatiques, électroniques et autres produits de technologie de l'informatique.</p>

